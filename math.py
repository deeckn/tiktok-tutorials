"""Numbers and Math"""

# Number Variables
x = 5
y = 2

# Arithmetic Operations
print(f"x = {x}, y = {y}")
print(f"x + y = {x+y}")  # 5 + 2 = 7
print(f"x - y = {x-y}")  # 5 - 2 = 3
print(f"x * y = {x*y}")  # 5 * 2 = 10
print(f"x / y = {x/y}")  # 5 / 2 = 2.5
print(f"x ** y = {x**y}")  # 5^2 = 25
print(f"x % y = {x%y}")  # 5 % 2 = 1


# Division
print(f"Float Division: x / y = {x/y}")  # Float

print(f"Integer Division: x // y = {x//y}")  # Integer

print(f"Division by zero: x / 0 = {x/0}")  # Error


# Type Conversion
number = "10"
number = int(number)
print(number + 1)
