"""Python Variables and Data Types"""

calories = 200

# Integer | ตัวเลขจำนวนเต็ม
age = 20

# Float | เลขที่มีการใช้จุดทศนิยม
height = 179.5

# String | ตัวอักษร หรือ คำ
name = "Dee"
color = 'Blue'

# Boolean | ค่าความจริง
handsome = True
rich = False

# List | รายการ (แก้ไขได้)
subjects = ["Math", "English", "Science"]
evens = [2, 4, 6, 8]

# Tuple | รายการ (แก้ไขไม่ได้)
coordinates = (-2, 3)
person = ("John", 18, "Male")

# Set | เซ็ท (ไม่มีสมาชิกซ้ำ)
# {1, 1, 1, 3, 5, 7} == {1, 3, 5, 7}
numbers = {1, 1, 1, 3, 5, 7}

# Dictionary | ข้อมูลชนิด key-value pair
fruits = {
    'key': 'value',
    'a': 'apple',
    'b': 'banana',
    'c': 'coconut'
}

# None Type
nothing = None
