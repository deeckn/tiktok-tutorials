"""
Python Strings:
Concatenation and
Escape Characters
"""

# Concatenation
hello = "Hello"
world = "World"
hello_world = hello + " " + world
print(hello_world)

# Multiline Strings
message = """
Hello, let's play with strings
As we can see,
Message goes across multiple lines.
"""
print(message)

# Escape Characters
new_line = "Hello\nWorld"
print(new_line)

tab = "Hello\tWorld"
print(tab)
